-- ── Public privileges and realtime invalidation only ───────────────────────

create function public_market_is_web_enabled(p_market_id uuid) returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from markets m
    join groups g on g.id = m.group_id
    where m.id = p_market_id
      and g.web_enabled
      and m.currency = 'sol'
      and not m.is_replay
  );
$$;

